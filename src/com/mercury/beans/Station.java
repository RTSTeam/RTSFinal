package com.mercury.beans;

public class Station {
	private int sid;
	private String stationAbbr;
	private String stationFullName;
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getStationAbbr() {
		return stationAbbr;
	}
	public void setStationAbbr(String stationAbbr) {
		this.stationAbbr = stationAbbr;
	}
	public String getStationFullName() {
		return stationFullName;
	}
	public void setStationFullName(String stationFullName) {
		this.stationFullName = stationFullName;
	}
	
	
}
